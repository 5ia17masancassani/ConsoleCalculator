package ch.bbw.ConsoleCalculator;

public class Calculator {

	protected int addition(int zahl1, int zahl2) {

		return zahl1 + zahl2;
	}

	protected int subtraktion(int zahl1, int zahl2) {

		return zahl1 - zahl2;
	}

	protected int multiplikation(int zahl1, int zahl2) {

		return zahl1 * zahl2;
	}

	protected int division(int zahl1, int zahl2) {

		return zahl1 / zahl2;
	}

}
